package com.libertymutualgroup.herman.util;

public class ArnUtil {

    private ArnUtil() {
        throw new IllegalAccessError("Utility class");
    }

    public static String getAccountFromArn(String arn) {
        String[] split = arn.split(":");
        return split[4];
    }
}
